namespace JulKali.Facebook.Messenger.Send
{
    /// <summary>
    /// Represents valid types of media attachments.
    /// </summary>
    public enum MediaAttachmentType
    {
        /// <summary>
        /// The media is an image file.
        /// </summary>
        Image,

        /// <summary>
        /// The media is an audio file.
        /// </summary>
        Audio,

        /// <summary>
        /// The media is a video file.
        /// </summary>
        Video,

        /// <summary>
        /// The media is a different type of file.
        /// </summary>
        File
    }
}