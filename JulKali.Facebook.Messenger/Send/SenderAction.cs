namespace JulKali.Facebook.Messenger.Send
{
    /// <summary>
    /// Represent available sender actions.
    /// </summary>
    public enum SenderAction
    {
        /// <summary>
        /// Display the typing bubble.
        /// </summary>
        TypingOn,

        /// <summary>
        /// Remove the typing bubble.
        /// </summary>
        TypingOff,

        /// <summary>
        /// Display the confirmation icon.
        /// </summary>
        MarkAsSeen
    }
}