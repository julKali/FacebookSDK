namespace JulKali.Facebook.Messenger.Send
{
    /// <summary>
    /// Represents valid webview height ratios when using Messenger Extensions.
    /// </summary>
    public enum WebviewHeightRatio
    {
        /// <summary>
        /// Opens the webview in a compact window.
        /// </summary>
        Compact,

        /// <summary>
        /// Opens the webview in a tall window.
        /// </summary>
        Tall,

        /// <summary>
        /// Opens the webview in a window that spans the full device height.
        /// </summary>
        Full
    }
}